char abc[120][120];
int m, n;
int i, j, k;
int ans;
int a[8] = {-1, -1, 0, 1, 1, 1, 0, -1};
int b[8] = {0, 1, 1, 1, 0, -1, -1, -1};

void dfs(int x, int y)
{
    int k;
    if (x < 0 || y < 0)
        return;
    if (abc[x][y] == '@')
    {
        abc[x][y] = '*';
        for (k = 0; k < 8; k++)
            dfs(x + a[k], y + b[k]);
    }
    return;
}

void solve()
{
    while (cin >> m >> n && (m || n))
    {
        ans = 0;
        for (i = 0; i < m; i++)
            cin >> abc[i];
        for (i = 0; i < m; i++)
        {
            for (j = 0; j < n; j++)
            {
                if (abc[i][j] == '@')
                {
                    ans++;
                    dfs(i, j);
                }
            }
        }
        cout << ans << endl;
    }
}

